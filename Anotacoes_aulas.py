# HTML = Parte mais simples do Front. 
# linguagem de marcação de hipertexto 
# utilizado para construir o esqueleto de 1 site.
# TAG HTML = Uma marcação utilizada para dizer o que é cada coisa: 
# Botão, imagem, titulo . Como se fossem pequenas peças que montam o Site. 
# Escrevendo uma Tag = <nome da tag> depois o conteúdo </nome da tag>
# Exemplo: <p>Eu sou um paragrafo </p> = Abertura, Conteúdo, Fechamento
# A tag pode ou nao ter conteúdo. Imagem nao tem conteúdo. 

# Atributo da TAG = Características da TAG. CLASSE, por exemplo:
# <p class = 'paragrafo'> Eu sou um paragrafo </p>
# class = 'paragrafo' == Atributo + Valor. 

# Estrutura HTML:
# 1. <HTML> = Raiz do documento
# 2. <HEAD> = Informações sobre documentação, por exemplo titulo
# Essas informações geralmente nao aparecem, exceto Titulo
# 3. <BODY> = Conteúdo da pagina, exibido ao usuario. 

